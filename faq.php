        <h5><p class="py-3"> Quickly. To be safe, you should talk to an attorney as soon as possible, especially if the injuries are severe or a death has resulted. All too often, valuable evidence disappears, witnesses move, memories grow dim and the practical ability to prove your case might diminish. When you are being treated by Accident Helpline, an attorney can also provide you with guidance concerning unpaid bills and getting needed treatment. Legal advice can also be useful if you have questions about the settlement value of a claim, your insurance policy’s terms, or suspect bad faith on the part of your insurance company. <br></p></h5>

