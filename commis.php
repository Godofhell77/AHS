  <div class="text-block"><br>
<h3>Mistake No. 2: Not Taking Care of Yourself After an Auto Accident</h3>
        <h5><p class="py-3"> After an auto accident and injury, you want to get your life back on track as quickly as possible. It’s easy to feel pressured by responsibilities at work, at home, and to your family. Life can quickly become overwhelming, and your own mental and physical needs get placed on the back burner. Not only does this do you a disservice by possibly slowing down your recovery time, it might also hurt your claim against the wrongdoer.<br>

A favorite argument of the other side is to claim that any failure/delay in obtaining medical treatment shows you weren’t injured in the accident. They argue that if you were “truly injured,” you would have sought immediate treatment from a health care provider.<br>

So be good to yourself. See a doctor if your injuries require it (e.g., you are in pain or having physical difficulties). Follow your doctor’s orders, treatment plan and physical therapy schedule. You’ll be glad you did. </p></h5><br>
</div>
</div>

<div class="thri-container" >
  <div class="text-block"><br>
<h3>Mistake No. 3: Alienating Your Doctors and Health Care Providers</h3>
        <h5><p class="py-3"> When you’re in pain and stressed out, it’s easy to take your frustrations out on your health care providers. This is a huge mistake. Your doctors and health care providers play a key role in putting your claim together. Next to you, your doctors and healthcare providers are your most critical witnesses.<br>

The amount of your settlement often depends on how likeable and credible you are, and the quality of your supporting medical evidence.<br>

Doctors are human beings. If you get on their bad side, their medical records, reports and deposition testimony might reflect poorly toward you and your injury. Do yourself a favor: Be nice to the people treating you. Be polite, respectful and friendly. Consider sending a thank you note after a particularly helpful visit or after completion of your physical therapy program. Try to pay your bills on time. If that’s not possible, consider working out a modest payment plan or setting up a lien arrangement. The importance of keeping these professionals in your corner cannot be overstated. </p></h5><br>
</div>
</div>

<div class="for-container" >
  <div class="text-block"><br>
<h3>Mistake No. 4: Forgetting To Preserve Evidence</h3>
        <h5><p class="py-3"> Photos can be incredibly helpful in proving your case. They often show things better than you can describe them, and they are difficult to contradict. As soon as you are able, take pictures of your injuries, your damaged vehicle, the location of your auto accident, and anything else related to your auto accident.<br>

Take as many pictures as you need to accurately capture the subject matter of your injury (e.g. a wound, scars, bruises, swelling, etc.). You might want to take several photos over time as your injuries heal. Throughout your case, you also want to save all medical records and bills — along with prescription bottles, orthopedic braces/splints/casts, and any other health care-related items.<br>

Make sure to keep track of any lost work time and wages, as well as changes in your job, job duties or salary. Save all receipts related to your personal injury (e.g. transportation costs, home care costs, etc.). It’s important to document any money you have to pay out or any wage loss you incur because of your accident. </p></h5><br>
